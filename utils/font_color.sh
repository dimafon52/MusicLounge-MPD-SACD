```bash
\e[38;5;190m
```

каждый кусок значит:

* `\e[` — начало управляющей ESC-последовательности.
* `38` — означает **устанавливаем цвет текста** (если было бы `48`, то это цвет фона).
* `;5;` — говорит: **мы используем 8-битную палитру (256 цветов)**.
* `;2;` - 24-битные (TrueColor, RGB)
* `190` — это номер цвета в палитре 0–255 (в данном случае светло-жёлто-зелёный).
* `m` — конец команды (Select Graphic Rendition).

То есть `38;5;190` буквально значит:
**“сделать цвет текста №190 из 256-палитры”**.

3. **24-битные (TrueColor, RGB)**

   ```
   \e[38;2;R;G;Bm      -  цвет текста
   \e[48;2;R;G;Bm      -  цвет фона 
   ```

---

⚡ Пример:

```bash
echo -e "\e[38;5;190mТекст 190\e[0m"
echo -e "\e[48;5;27m\e[38;5;15m Белый текст на синем фоне \e[0m"
```

   Здесь вместо `;5;` пишем `;2;`, и дальше идут **три числа** — красный, зелёный, синий (`0–255`).
   Пример:

   * `\e[38;2;255;128;0m` → оранжевый текст
   * `\e[48;2;50;200;50m` → зелёный фон
   
